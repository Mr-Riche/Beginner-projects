num_1 = int(input(">>>"))
num_2 = int(input(">>>"))
operation = input("operation")
if operation == "+":
    print(
        num_1 + num_2
    )
elif operation == "-":
    print(
        num_1 - num_2
    )

elif operation == "/":
    print(
        num_1 / num_2
    )
elif operation == "*":
    print(
        num_1 * num_2
    )

